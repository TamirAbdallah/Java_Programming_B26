package ExtraPractice;

public class GalonsToLiters {

    /*
    create a class called GalonsToLiters, and write a program that can convert
the gallons to litters
Ex:
gallon = 10;
output:
10 gallons equal to 37.8541 liters
gallon = 20
output:
20 gallons equal to 75.7082 liters
     */
    public static void main(String[] args) {
        double LitersToGallons = 3.78541;
        double Gallons1 = 10;
        double Gallons2 = 20;
        System.out.println("10 Liters Equal to Gallons " + LitersToGallons * Gallons1 );
        System.out.println("20 Liters Equal to Gallons " + LitersToGallons * Gallons2 );


    }
}
