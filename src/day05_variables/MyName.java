package day05_variables;

public class MyName {
    public static void main(String[] args) {


        char first  = 'T';
        char second = 'a';
        char third  = 'm';
        char fourth = 'i';
        char fifth  = 'r';

        System.out.println(first);
        System.out.println(second);
        System.out.println(third);
        System.out.println(fourth);
        System.out.println(fifth);

        System.out.println();

        System.out.println(" My name : " + first + second + third + fourth + fifth); // or you can make a variable of the name

        String name = " " + first + second + third + fourth + fifth;    // string name = "Tamir"
        System.out.println("My name: " + name);









    }
}
